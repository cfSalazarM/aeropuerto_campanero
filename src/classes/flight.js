export function Flight(code, plane, pilot, cityOrigin, cityDestiny, date, time, state, airline) {
    this.code = code;
    this.plane = plane;
    this.pilot = pilot;
    this.cityOrigin = cityOrigin;
    this.cityDestiny = cityDestiny;
    this.date = date;
    this.time = time;
    this.state = state;
    this.airline = airline;
}